#!/usr/bin/env ruby
# frozen_string_literal: true

# Generates a large, self-contained SCSS file that exercises a Sass compiler
# with non-trivial per-compile work: big @each lists, nested loops, color
# functions, interpolation, mixin includes. No network, deterministic output.
#
# Usage: ruby gen_corpus.rb [N_COMPONENTS] [OUTFILE]
#   N_COMPONENTS defaults to 400; output defaults to
#   bench/corpus/generated/large.scss
#
# Each "component" emits a themed set of rules across several color variants
# and breakpoints, so the rule count is roughly N_COMPONENTS * variants * a
# handful of selectors. With the defaults this produces a file that takes
# dart-sass a meaningful fraction of a second to compile.

n = (ARGV[0] || "400").to_i
out = ARGV[1] || File.expand_path(
  "../corpus/generated/large.scss", __dir__
)

# A palette of base hues we rotate through deterministically.
HUES = %w[
  #e53935 #d81b60 #8e24aa #5e35b1 #3949ab #1e88e5 #039be5 #00acc1
  #00897b #43a047 #7cb342 #c0ca33 #fdd835 #ffb300 #fb8c00 #f4511e
].freeze

VARIANTS = %w[base hover active focus disabled].freeze
SIZES    = { "sm" => "0.75rem", "md" => "1rem", "lg" => "1.25rem", "xl" => "1.5rem" }.freeze

File.open(out, "w") do |f|
  f.puts "// AUTO-GENERATED by scripts/gen_corpus.rb -- do not edit by hand."
  f.puts "// Components: #{n}"
  f.puts "@use \"sass:math\";"
  f.puts

  # Build a big Sass list of (name, color) pairs that we'll @each over.
  f.puts "$components: ("
  n.times do |i|
    color = HUES[i % HUES.length]
    f.puts "  (\"c#{i}\", #{color}),"
  end
  f.puts ");"
  f.puts

  f.puts "$sizes: ("
  SIZES.each { |k, v| f.puts "  \"#{k}\": #{v}," }
  f.puts ");"
  f.puts

  # A mixin that does real per-include work: color math + interpolation.
  f.puts <<~SCSS
    @mixin themed($name, $base) {
      .comp-#{'#{$name}'} {
        --comp: #{'#{$name}'};
        color: darken($base, 20%);
        background-color: lighten($base, 30%);
        border: 1px solid mix($base, #000, 80%);

        &:hover   { background-color: lighten($base, 20%); }
        &:active  { background-color: darken($base, 6%); }
        &::before { content: "#{'#{$name}'}"; color: rgba($base, 0.6); }

        @each $sz, $val in $sizes {
          &.size-#{'#{$sz}'} {
            font-size: $val;
            padding: math.div($val, 2) $val;
            border-radius: math.div($val, 4);
          }
        }
      }
    }
  SCSS
  f.puts

  # The main loop: for every component, include the mixin and also emit a
  # nested grid + variant block so we generate thousands of rules.
  f.puts "@each $name, $base in $components {"
  f.puts "  @include themed($name, $base);"
  f.puts
  f.puts "  .grid-#{'#{$name}'} {"
  f.puts "    display: grid;"
  f.puts "    @for $col from 1 through 6 {"
  f.puts "      .col-#{'#{$col}'} {"
  f.puts "        grid-column: span $col;"
  f.puts "        width: percentage(math.div($col, 6));"
  f.puts "      }"
  f.puts "    }"
  f.puts "  }"
  f.puts "}"
end

# Report rough size so the human running it has a sense of scale.
lines = File.foreach(out).count
bytes = File.size(out)
warn "Wrote #{out}: #{lines} lines, #{bytes} bytes, #{n} components"
